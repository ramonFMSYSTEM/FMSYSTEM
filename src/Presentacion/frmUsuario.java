    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if (txtnombreu.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debe ingresar Nombre de Usuario");
            txtnombreu.requestFocus();
            return;
        }
        if (txtApellidopu.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debe ingresar Apellido Paterno");
            txtApellidopu.requestFocus();
            return;
        }
         if (txtApellidomu.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debe ingresar Apellido Materno");
            txtApellidomu.requestFocus();
            return;
        }
        if (txtTelefonou.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debe ingresar Telefono");
            txtTelefonou.requestFocus();
            return;
        }

        if (txtDireccionu.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debe ingresar Direccion");
            txtDireccionu.requestFocus();
            return;
        }

          if (txtEmailu.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debe ingresar Email");
            txtEmailu.requestFocus();
            return;
        }
          
         if (txtpContrasenau.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debe ingresar Contraseña");
            txtpContrasenau.requestFocus();
            return;
        }

        Dusuario dts = new Dusuario();
        Lusuario func = new Lusuario();

        dts.setNombreu(txtnombreu.getText());
        dts.setApellidopu(txtApellidopu.getText());
        dts.setApellidomu(txtApellidomu.getText());
        dts.setTelefonou(txtTelefonou.getText());
        dts.setDireccionu(txtDireccionu.getText());
        int seleccionado = cboPuesto.getSelectedIndex();
        dts.setPuestou((String) cboPuesto.getItemAt(seleccionado));
        dts.setEmailu(txtEmailu.getText());
        dts.setContrasenau(txtpContrasenau.getText());

        if (accion.equals("guardar")) {
            if (func.InsertarUsuario(dts)) {
                JOptionPane.showMessageDialog(rootPane, "El usuario fue registrado con exito ");
                MostrarUsuariosI("");
                InhabilitarComponentes();
            }

        } else if (accion.equals("editar")) {
            dts.setIdusuario(Integer.parseInt(txtidusuario.getText()));

            if (func.EditarUsuario(dts)) {
                JOptionPane.showMessageDialog(rootPane, "El registro de Usuario  fue editado con exito ");
                MostrarUsuariosI("");
                InhabilitarComponentes();
            }
        }
// TODO add your handling code here:
    }//GEN-LAST:event_btnGuardarActionPerformed

