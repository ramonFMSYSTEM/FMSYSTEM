    private void txtnombreuKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtnombreuKeyTyped
        // TODO add your handling code here:
 String s1=String.valueOf(evt.getKeyChar());
        
        if(!(s1.matches("[\ba-zA-Záéóíú.ÁÉÍÓÚñÑ ]"))){
            evt.consume();
            getToolkit().beep();
             JOptionPane.showMessageDialog(this, "Ingrese letras");
            //Regresa cursor al campo 
            txtnombreu.setCursor(null);
            txtnombreu.setCaretColor(Color.red);
        }

    }//GEN-LAST:event_txtnombreuKeyTyped

